func getLastMoment(n int, left []int, right []int) int {
	minL, maxR := n, 0
	for _, num := range right {
		minL = min(minL, num)
	}
	for _, num := range left {
		maxR = max(maxR, num)
	}
	return max(n-minL, maxR)
}

func min(x, y int) int {
	if x < y {
		return x
	}
	return y
}

func max(x, y int) int {
	if x > y {
		return x
	}
	return y
}
